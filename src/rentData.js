/* Resolve a 5-digit zip to a state abbreviation for local fallback. */
export function zipToState(zip) {
  if (!zip || !/^\d{5}$/.test(zip)) return null;
  return ZIP3_STATE[zip.slice(0, 3)] || null;
}

/* Model a by-bedroom breakdown from a median (live Census data overrides this). */
export function modelByBedroom(median) {
  if (!median) return null;
  const r10 = (n) => Math.round(n / 10) * 10;
  return {
    studio: r10(median * 0.82),
    br1:    r10(median * 0.9),
    br2:    r10(median * 1.12),
    br3:    r10(median * 1.36),
  };
}
